'''
    Dairenin alanı = pi*r*r
    Dairenin çevresi = 2*pi*r

    Yarıçapı girilen bir dairenin alanını ve çevresini hesaplayınız.

'''
r = float(input("Yarıçap:"))
pi = 3.14

daireninCevresi = 2*pi*r
daireninAlani = pi*r*r

print("Dairenin Alanı:" , daireninAlani, "Dairenin Çevresi:", daireninCevresi)

